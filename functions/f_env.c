#include "../utils/lib.h"

char	f_env(char **env)
{
	while (*env)
	{
		if ((*env)[ft_strlen(*env, '=')])
		{
			ft_putstr_fd(*env, 1);
			ft_putchar_fd('\n', 1);
		}
		env++;
	}
	return (0);
}
